package com.sim.basicres.views.particleView;

/**
 * Github: https://github.com/JeasonWong/Particle
 */
public class Particle {

    public float x;
    public float y;
    public float radius;

    public Particle() {
    }

    public Particle(float x, float y, float radius) {
        this.x = x;
        this.y = y;
        this.radius = radius;
    }
}
